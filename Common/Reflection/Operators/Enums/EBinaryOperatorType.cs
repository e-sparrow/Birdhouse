namespace Birdhouse.Common.Reflection.Operators.Enums
{
    public enum EBinaryOperatorType
    {
        Addition, // x + y
        Subtraction, // x - y
        Multiply, // x * y
        Division, // x / y
        Modulus, // x % y
        BitwiseAnd, // x & y
        BitwiseOr, // x | y
